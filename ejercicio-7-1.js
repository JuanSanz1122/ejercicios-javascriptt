let x = prompt("digite un numero del 1 al 7")
switch (x){
    case"1": alert("lunes")
    break
    case"2": alert("martes")
    break
    case"3": alert("miercoles")
    break
    case"4": alert("jueves")
    break
    case"5": alert("viernes")
    break
    case"6": alert("sabado")
    break
    case"7": alert("domingo")
    break
    default: alert("no hay dias con ese numero")
}